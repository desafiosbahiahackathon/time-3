    {
        $users = User::pluck('name', 'id');
        $woman = Woman::pluck('name', 'id');
        $aggressor = Aggressor::pluck('name', 'id');

        return view('visit/create')
                ->withUsers($users)
                ->withWoman($woman)
                ->withAggressor($aggressor);
    }

